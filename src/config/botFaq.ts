import { CONTACT } from '@/data/contact'

export const QUICK_REPLIES = [
  'ค่าบริการ',
  'พื้นที่บริการ',
  'นัดหมายพบทนาย',
  'เอกสารที่ต้องเตรียม',
  'เวลาทำการ',
  'ช่องทางติดต่อ',
] as const

export function getBotReply(q: string) {
  const s = q.toLowerCase()

  if (/(ค่า|เรท|ราคา)/.test(s)) {
    return [
      'ค่าบริการขึ้นกับประเภทคดีและขั้นตอนที่ต้องดำเนินการ',
      '— เบื้องต้นสามารถประเมินฟรีจากรายละเอียดคดีของท่าน',
      `โทร: ${CONTACT.phone} · อีเมล: ${CONTACT.email} · LINE: ${CONTACT.lineOfficial}`,
    ].join('\n')
  }

  if (/พื้นที่|ต่างจังหวัด|ทั่วราชอาณาจักร/.test(s)) {
    return `ให้บริการทั่วราชอาณาจักรครับ/ค่ะ (สามารถประชุมออนไลน์และลงพื้นที่ตามนัดหมายได้)`
  }

  if (/นัด|คิว|พบ/.test(s)) {
    return [
      'นัดหมายพบทนายได้ทาง /contact หรือโทร/ไลน์ตามข้อมูลติดต่อ',
      `โทร: ${CONTACT.phone} · LINE: ${CONTACT.lineOfficial}`,
    ].join('\n')
  }

  if (/เอกสาร|เตรียม|ต้องใช้/.test(s)) {
    return [
      'เอกสารเบื้องต้นที่ควรเตรียม:',
      '• บัตรประชาชน, ทะเบียนบ้าน',
      '• เอกสารคดี/สัญญา/หลักฐานที่เกี่ยวข้อง (ไฟล์หรือภาพถ่ายก็ได้)',
      '• ข้อมูลคู่สัญญาหรือคู่ความ',
      'หากไม่ครบสามารถส่งเพิ่มเติมภายหลังได้',
    ].join('\n')
  }

  if (/เวลา|เปิด|ทำการ/.test(s)) {
    return 'ติดต่อได้ตลอด 24 ชม. (เพื่อนัดหมาย/ให้คำปรึกษาเบื้องต้น)'
  }

  if (/ติดต่อ|contact|ช่องทาง/.test(s)) {
    return `โทร: ${CONTACT.phone}\nอีเมล: ${CONTACT.email}\nLINE Official: ${CONTACT.lineOfficial}\nนัดหมาย: /contact`
  }

  // ค่าเริ่มต้น
  return 'ขอบคุณที่ติดต่อครับ/ค่ะ กรุณาพิมพ์ “ค่าบริการ”, “นัดหมายพบทนาย”, “เอกสารที่ต้องเตรียม”, “ช่องทางติดต่อ” หรือส่งรายละเอียดคดีสั้น ๆ มาได้เลย'
}
